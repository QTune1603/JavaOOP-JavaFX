package JavaSQL;

import java.util.Scanner;

/**
 *
 * @author DELL
 */
public class toanTuSoSanh {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        //toan tu so sanh
        System.out.println(10 < 20);
        System.out.println(10 == 20);
        System.out.println(10 != 20);
        System.out.println(10 <= 20);
        System.out.println(10 >= 20);
        /*
        //toan tu logic : AND(&&) OR(||) NOT(!)
        //toan tu tang/giam : n++ , n--, --n, ++n
        //toan tu ba ngoi: 
        vd: 2 < 3 ? 50 : 100
            => 2 < 3 la dung hay sai, neu dung in ra 50, sai in ra 100
        
        */
    }
}
